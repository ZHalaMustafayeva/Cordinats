function getCylinderPath(x, y, height, radius) {
    const halfHeight = height;
    const topSemiCircle = `M${x - radius},${y - halfHeight} A${radius},${radius} 0 0,0 ${x + radius},${y - halfHeight}`;
    const bottomSemiCircle = `M${x + radius},${y + halfHeight} A${radius},${radius} 0 0,0 ${x - radius},${y + halfHeight}`;
    const leftSide = `L${x - radius},${y - halfHeight} L${x - radius},${y + halfHeight}`;
    const rightSide = `L${x + radius},${y + halfHeight} L${x + radius},${y - halfHeight} Z`;
    return topSemiCircle + leftSide + bottomSemiCircle + rightSide;
}

